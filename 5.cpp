#include<iostream>
using namespace std;
int main()
{
    int a;
    cout<<"Size of int is:"<<sizeof(a);

    float b;
    cout<<"Size of float is:"<<sizeof(b);
    
    double c;
    cout<<"Size of double is:"<<sizeof(c);
    
    char d;
    cout<<"Size of char is:"<<sizeof(d);
    
    return 0;
}
